"""
Tipos de dados
str / int / float / bool
typecashing = conversão de um tipo para outro
"""
print('Luiz', type('Luiz'))
print(10, type(10))
print(0.0, type(0.0))
print('l' == 'L', type('l'=='L'))
print(bool(""))
print('Luiz', type('Luiz'), bool('Luiz'))

# String: nome
print('Iuri', type('Iuri'))
# Idade: int
print(36, type(36))
# Altura: float
print(1.86, type(1.86))
# É maior de idade 10 > 20
print(36>18, type(36>18))

